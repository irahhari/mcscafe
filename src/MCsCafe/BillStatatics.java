    private void FindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        Statement stmt = null;
        ResultSet rs = null;
        if (StartDate.getDate() != null && EndDate.getDate() != null) {
            try {
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                String start = format.format(StartDate.getDate());
                String end = format.format(EndDate.getDate());
                Class.forName("java.sql.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/mcscafe", "mcscafe", "hari");
                stmt = con.createStatement();
                String query = "SELECT COUNT(*), SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' and BillNo <= '" + end + "';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Bill.setText(rs.getString(1));
                    Sales.setText(rs.getString(2));
                }
                query = "SELECT COUNT(*), SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' AND BillNo <= '" + end + "' AND Place = 'P';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Parcel.setText(rs.getString(1));
                    ParcelAmount.setText(rs.getString(2));
                }
                query = "SELECT SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' AND BillNo <= '" + end + "' AND Method = NULL;";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    PPending.setText(rs.getString(1));
                }
                query = "SELECT SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' AND BillNo <= '" + end + "' AND Method = 'U';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    UPI.setText(rs.getString(1));
                }
                query = "SELECT SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' AND BillNo <= '" + end + "' AND Method = 'B';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Card.setText(rs.getString(1));
                }
                query = "SELECT SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' AND BillNo <= '" + end + "' AND Method = 'C';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Cash.setText(rs.getString(1));
                }
                query = "SELECT SUM(Price) FROM Bill WHERE BillNo >= '" + start + "' AND BillNo <= '" + end + "' AND Method = 'P';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Postpaid.setText(rs.getString(1));
                }
                query = "SELECT SUM(Amount) FROM Purchase WHERE PurchaseNo >= '" + start + "' AND PurchaseNo <= '" + end + "';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Purchase.setText(rs.getString(1));
                }
                query = "SELECT SUM(Pending) FROM Postpurchase WHERE PurchaseNo >= '" + start + "' AND PurchaseNo <= '" + end + "';";
                rs = stmt.executeQuery(query);
                if (rs.next()) {
                    Pending.setText(rs.getString(1));
                }
            } catch (ClassNotFoundException | SQLException e) {
                try {
                    Files.write(Paths.get(System.getProperty("user.dir") + "\\lib\\log.txt"), LoginPage.encrypt(new java.util.Date(System.currentTimeMillis()).toString()
                            + " BillStatatics:376:FindActionPerformed\n" + e.toString() + "\n\n").getBytes(), StandardOpenOption.APPEND);
                } catch (IOException ex) {
                }
            } finally {
                if (con != null) {
                    try {
                        con.close();
                    } catch (SQLException ex) {
                    }
                }
                if (stmt != null) {
                    try {
                        stmt.close();
                    } catch (SQLException ex) {
                    }
                }
                if (rs != null) {
                    try {
                        rs.close();
                    } catch (SQLException ex) {
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter start and end date", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_FindActionPerformed

